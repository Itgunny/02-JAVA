public class NotDemo {
    public static void main(String[] args) {
        // '!'는 부정을 의미한다. '같다'의 부정은 '같지 않다'이다. 이것을 기호로는 '!='로 표시한다.
        System.out.println(1 != 2);
        System.out.println(1 != 1);
        System.out.println("one" != "two");
        System.out.println("one" != "one");
    }
}
