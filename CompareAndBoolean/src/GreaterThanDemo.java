public class GreaterThanDemo {

    public static void main(String[] args) {
        // 좌항이 우항보다 크다면 참, 그렇지 않다면 거짓임을 알려주는 연산자다. '<'는 반대의 의미로 언급은 생략하겟다.
        System.out.println(10 > 20);
        System.out.println(10 > 2);
        System.out.println(10 > 10);
    }
}
